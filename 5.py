# 5. Escreva um programa que calcule a área de um retângulo.
a = (float(input("Digite o valor de um dos lados do retângulo: ")))
b = (float(input("Digite o valor do outro lado do retângulo: ")))
area = (a * b)
print("A área do retângulo, cujo seus lados medem {} e {} é igual a {}".format(a, b, area))